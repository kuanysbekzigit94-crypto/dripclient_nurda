def get_text(lang: str, key: str) -> str:
    """Get translated text by language and key."""
    if lang not in TRANSLATIONS:
        lang = "kk"
    return TRANSLATIONS[lang].get(key, f"[{key}]")

def get_all_translations(key: str = None) -> list:
    """Get all translations for a key across all languages."""
    if key is None:
        return TRANSLATIONS
    
    result = []
    for lang in TRANSLATIONS:
        if key in TRANSLATIONS[lang]:
            result.append(TRANSLATIONS[lang][key])
    return result
